func halfBlock(_ top: Bool, _ bot: Bool) -> Character {
    switch (top, bot) {
    case (false, false): return " "
    case (true,  false): return "\u{2580}"  // ▀
    case (false, true):  return "\u{2584}"  // ▄
    case (true,  true):  return "\u{2588}"  // █
    }
}

func render(_ text: String, maxWidth: Int) -> [String] {
    let upper = text.uppercased()
    var rows = ["", "", ""]

    for ch in upper {
        let bitmap: [String]
        if let g = glyphs[ch] {
            bitmap = g
        } else {
            // Unknown char → dotted placeholder
            bitmap = [".###.", "#.#.#", ".###.", "#.#.#", ".###.", "#.#.#"]
        }

        let w = bitmap[0].count
        // Stop if next char would overflow terminal width
        if rows[0].count + w + 1 > maxWidth { break }

        for pair in 0..<3 {
            let topChars = Array(bitmap[pair * 2])
            let botChars = Array(bitmap[pair * 2 + 1])
            for col in 0..<w {
                let t = topChars[col] == Character("#")
                let b = botChars[col] == Character("#")
                rows[pair].append(halfBlock(t, b))
            }
            rows[pair] += " "  // inter-character spacing
        }
    }

    return rows
}

// ── Main: pipe mode ──
// Read width from first argument, default 120
let maxW = CommandLine.arguments.count > 1
    ? Int(CommandLine.arguments[1]) ?? 120
    : 120

while let line = readLine(strippingNewline: true) {
    if line.isEmpty {
        print(" ")
        print(" ")
        print(" ")
    } else {
        let rows = render(line, maxWidth: maxW)
        for row in rows {
            print(row)
        }
    }
    print("---END---")
    fflush(stdout)
}
